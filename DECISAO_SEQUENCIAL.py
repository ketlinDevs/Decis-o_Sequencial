print('Decisão Sequencial')

n = int(input("Digite um numero: "))

if n == 1:
    print("Você digitou o valor 1")

elif n == 2:
    print("Você digitou o valor 2")

elif n < 1:
    print("Você digitou um valor muito baixo")

elif n > 2:
    print("Você digitou um valor muito alto")

print("Fim")